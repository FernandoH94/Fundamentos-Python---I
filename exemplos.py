# Exemplo 1: Adição e Subtração
a = 10
b = 5

soma = a + b
subtracao = a - b

print("Soma:", soma)
print("Subtração:", subtracao)

# Exemplo 2: Multiplicação e Divisão
x = 6
y = 1

multiplicao = x * y
divisao = x / y

print("Multiplicação:", multiplicao)
print("Divisão:", divisao)

# Exemplo 3: Divisão inteira e Módulo
num1 = 17
num2 = 4

div_inteira = num1 // num2
resto = num1 % num2

print("Divisão inteira:", div_inteira)
print("Resto da divisão:", resto)

# Exemplo 4: Potenciação
base = 2
expoente = 3

potencia = base ** expoente

print("Potência:", potencia)